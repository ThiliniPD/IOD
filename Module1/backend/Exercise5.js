// creating an array of 5 elements
let fruits = [ "apples", "grapes", "mangoes", "bananas", "strawberries" ];
console.log(fruits);


// replacing the value of the element at position 1 and 4
fruits[1] = "pears";
fruits[4] = "watermelons"
console.log(fruits);

// add a new element to the beginning of array
fruits.unshift('papayas');
console.log(fruits);

// remove the element at the end of the array
fruits.pop();
console.log(fruits);

// print the final array to the console
console.log(fruits);